package com.example.unitcoverter;

public class Converter {
    public static double toCelcius(double fah) { return (fah - 32) * 5/9;}

    public static double toKG (double lbs) { return lbs * 0.45359237;}

    public static double toKil (double miles) { return miles * 1.609;}

    public static double toMet (double ft) { return ft / 3.281;}


}
